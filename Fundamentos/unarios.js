let num1 = 1
let num2 = 2

console.log(num1)
num1++ //Adiciona uma unidade
console.log(num1)

console.log(--num1 === num2--) //False
console.log(num1 === num2) //True
// O código compara primeiro o numero e depois faz a operação unaria pois existe uma ordem de procedencia
